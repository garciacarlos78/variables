package exercici_variables;

public class Fase1 {

	public static void main(String[] args) {
		
		// String vars
		String nom = "Carlos";
		String cognom1 = "Garcia";
		String cognom2 = "Romero";
		
		// int vars
		int dia = 27;
		int mes = 12;
		int any = 1978;
		
		// Show string vars
		System.out.println(cognom1 + " " + cognom2 + ", " + nom);
		
		// Show int vars
		System.out.println(dia + "/" + mes + "/" + any);

	}

}
